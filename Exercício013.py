diasemana = int(input('Digite um número entre 1 e 7:\n'))


if diasemana == 1:
    print('Domingo')

elif diasemana == 2:
    print('Segunda-Feira')

elif diasemana == 3:
    print('Terça-Feira')

elif diasemana == 4:
    print('Quarta-Feira')

elif diasemana == 5:
    print('Quinta-Feira')

elif diasemana == 6:
    print('Sexta-Feira')

elif diasemana == 7:
    print('Sábado')

else:
    print('Número inválido')
